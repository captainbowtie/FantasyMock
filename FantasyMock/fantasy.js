function validateDivision() {
  var divisionList = [0, 0];
  for (var a = 0; a < 8; a++) {
    if ($("#division" + a).html() == "Cedar Rapids") {
      divisionList[0]++;
    } else if ($("#division" + a).html() == "Hamilton") {
      divisionList[1]++;
    }
  }
  if (divisionList[0] != 4 || divisionList[1] != 4) {
    $("#divisionValidator").html("🔴");
    return false;
  } else {
    $("#divisionValidator").html("🔵");
    return true;
  }
}

function validateCost() {
  var maxCost = 200.0;
  var totalCost = 0.0;
  for (var a = 0; a < 8; a++) {
    var teamCost = Number($("#cost" + a).html());
    totalCost += teamCost;
  }
  $("#totalCost").html(totalCost);
  if (totalCost > maxCost) {
    return false;
  } else {
    return true;
  }
}

function containsDuplicates(array) {
  var counts = [];
  for (var i = 0; i <= array.length; i++) {
    if (counts[array[i]] === undefined) {
      counts[array[i]] = 1;
    } else {
      return true;
    }
  }
  return false;
}

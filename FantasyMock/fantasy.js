var selectOptions = "";
for (var a = 0; a < data.length; a++) {
  selectOptions +=
    "<option>" + data[a].teamNumber + " " + data[a].school + "</option>\n";
}

$(document).ready(function() {
  for (var a = 0; a < 8; a++) {
    $("#select" + a).append(selectOptions);
  }
});

$(".teamSelect").on("change", function() {
  //Get team number
  var row = this.id.substring(6, 7);
  var team = this.value.substring(0, 4);
  var teamIndex = 0;

  //Determine team's index in the data array
  for (var a = 0; a < data.length; a++) {
    if (team == data[a].teamNumber) {
      teamIndex = a;
      a = data.length;
    }
  }

  //Set the teams division
  $("#division" + row).html(data[teamIndex].orcs);

  //Determine whether cost should be raw cost or holdover cost
  var cost;
  if ($("#orcs" + row).prop("checked")) {
    cost = data[teamIndex].holdoverCost;
  } else {
    cost = data[teamIndex].nctCost;
  }
  $("#cost" + row).html(cost);

  validateDivision();
  validateCost();
});

$(".check").on("click", function() {
  //Find team number of the team in question
  var row = this.id.substring(4, 5);
  var team = $("#select" + row)
    .val()
    .substring(0, 4);

  //Determine that team's index in the data array
  for (var a = 0; a < data.length; a++) {
    if (team == data[a].teamNumber) {
      teamIndex = a;
      a = data.length;
    }
  }

  //Set the cost of the team based on whether holdover or not
  if ($(this).prop("checked")) {
    var newValue = data[teamIndex].holdoverCost;
  } else {
    var newValue = data[teamIndex].nctCost;
  }
  $("#cost" + row).html(newValue);
});

